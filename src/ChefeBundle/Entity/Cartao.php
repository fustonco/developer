    private $numero;

    /**
     * @var string
     */
    private $bandeira;

    /**
     * @var \DateTime
     */
    private $vencimento;

    /**
     * @var string
     */
    private $cvc;

    /**
     * @var \DateTime
     */
    private $melhorData;

    /**
     * @var boolean
     */
    private $active;

    /**
     * @var integer
     */
    private $id;


    /**
     * Set titulo
     *
     * @param string $titulo
     * @return Cartao
     */
    public function setTitulo($titulo)
    {
        $this->titulo = $titulo;

        return $this;
    }

    /**
