    /**
     * Get numero
     *
     * @return string 
     */
    public function getNumero()
    {
        return $this->numero;
    }

    /**
     * Set bandeira
     *
     * @param string $bandeira
     * @return Cartao
     */
    public function setBandeira($bandeira)
    {
        $this->bandeira = $bandeira;

        return $this;
    }

    /**
